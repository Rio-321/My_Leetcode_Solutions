class Solution
{
    public:
    //Function to check if a string is Isogram or not.
    bool isIsogram(string s)
    {
         unordered_set<char> st;
         
         for(auto x: s)
         {
             if(st.find(x) != st.end())
             return 0;
             
             st.insert(x);
         }
         
         return 1;
    }
};

